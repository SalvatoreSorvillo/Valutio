  function translate(text, lang) {
    if (lang !== "it" || text == null) return text;
    var raw = String(text);
    if (!raw.trim()) return raw;
    var left = raw.match(/^\s*/)[0], right = raw.match(/\s*$/)[0];
    var body = normalize(raw);
    var exact = IT.exact[body];
    if (exact != null) return left + polishItalianText(exact) + right;
    for (var i = 0; i < IT.patterns.length; i++) {
      var rule = IT.patterns[i], hit = body.match(rule[0]);
      if (hit) {
        var repl = typeof rule[1] === "function" ? rule[1].apply(null, hit) : body.replace(rule[0], rule[1]);
        return left + polishItalianText(repl) + right;
      }
    }
    return raw;
  }

  function skipElement(el) {
    if (!el || !el.tagName) return false;
    var tag = el.tagName.toLowerCase();
    return tag === "script" || tag === "style" || tag === "noscript" || tag === "svg" || tag === "canvas" || tag === "code" || tag === "pre";
  }
  function shouldTranslateTextNode(node) {
    for (var p = node.parentElement; p; p = p.parentElement) {
      if (skipElement(p) || p.hasAttribute("data-no-i18n")) return false;
    }
    return true;
  }
  function apply(root, lang) {
    lang = lang === "it" ? "it" : "en";
    try { document.documentElement.lang = lang; } catch (e) {}
    if (lang !== "it") return;
    root = root || document.body;
    var base = root.nodeType === 1 || root.nodeType === 9 ? root : document.body;
    var nodes = [];
    var walker = document.createTreeWalker(base, NodeFilter.SHOW_TEXT, {
      acceptNode: function (node) {
        if (!node.nodeValue || !node.nodeValue.trim()) return NodeFilter.FILTER_REJECT;
        return shouldTranslateTextNode(node) ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT;
      }
    });
    while (walker.nextNode()) nodes.push(walker.currentNode);
    nodes.forEach(function (node) {
      var next = translate(node.nodeValue, lang);
      if (next !== node.nodeValue) node.nodeValue = next;
    });
    var attrs = ["placeholder", "title", "aria-label"];
    var els = base.querySelectorAll ? base.querySelectorAll("[placeholder],[title],[aria-label],input[type=button],input[type=submit]") : [];
    Array.prototype.forEach.call(els, function (el) {
      if (skipElement(el) || el.hasAttribute("data-no-i18n")) return;
      attrs.forEach(function (attr) {
        if (!el.hasAttribute(attr)) return;
        var old = el.getAttribute(attr), next = translate(old, lang);
        if (next !== old) el.setAttribute(attr, next);
      });
      if ((el.type === "button" || el.type === "submit") && el.value) {
        el.value = translate(el.value, lang);
      }
    });
  }

